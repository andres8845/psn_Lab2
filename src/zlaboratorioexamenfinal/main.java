    private void deactivateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deactivateButtonActionPerformed
        String username = deactivateUserTF.getText();
        if(psn.users.search(username)==-1){
            JOptionPane.showMessageDialog(null, "Usuario no existe!");
        }
        try {
            psn.deactivateUsers(username);
             loadUsers();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "Algo salio mal!");
        }
       
    }//GEN-LAST:event_deactivateButtonActionPerformed
    
    private void loadUsers() throws IOException {
        isActivo=false;
        entry usuarios=psn.users.inicio;
        combo.removeAllItems();

        while(usuarios!=null){
            combo.addItem(usuarios.usuario);
            usuarios=usuarios.siguiente;
        }
        isActivo=true;
        updateContent();

    }
    
    

    private void addTrophyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addTrophyButtonActionPerformed
        if(addTrophyTF.getText().isEmpty() || nameTrophy.getText().isEmpty() || trophyType1.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null, "Llenar todas las casillas!");
        }else{
            hashtable.trofeos tipoTrofeo = null;
            String trofeo;
            trofeo=trophyType1.getText().toLowerCase();
            if(trofeo.equals("platino")){
            tipoTrofeo=tipoTrofeo.platino;
            }else if (trofeo.equals("oro")){
                tipoTrofeo=tipoTrofeo.oro;
            }else if (trofeo.equals("plata")){
                tipoTrofeo=tipoTrofeo.plata;
            }else if(trofeo.equals("bronce")){
                tipoTrofeo=tipoTrofeo.bronce;
            }else{
                JOptionPane.showMessageDialog(null, "Trofeo no existe!");
            }
            String gameName = juego.getText();
            String trophName = nameTrophy.getText();
            String username = addTrophyTF.getText();
            try {
                psn.addTrophieTo(username, gameName, trophName, tipoTrofeo);
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Algo salio mal!");
            }
        }
    }//GEN-LAST:event_addTrophyButtonActionPerformed

